package name.qd.tradingPlatform.strategies;

public class Book {
	private double bPrice;
	private double bQty;
	private double sPrice;
	private double sQty;

	public double getbPrice() {
		return bPrice;
	}

	public void setbPrice(double bPrice) {
		this.bPrice = bPrice;
	}

	public double getbQty() {
		return bQty;
	}

	public void setbQty(double bQty) {
		this.bQty = bQty;
	}

	public double getsPrice() {
		return sPrice;
	}

	public void setsPrice(double sPrice) {
		this.sPrice = sPrice;
	}

	public double getsQty() {
		return sQty;
	}

	public void setsQty(double sQty) {
		this.sQty = sQty;
	}
}
